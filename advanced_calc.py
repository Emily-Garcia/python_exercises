num1 = float(input('Ingresa el primer numero: '))
num2 = float(input('Ingresa el segundo numero: '))

suma = num1+num2
print('El resultado de la suma es',suma)

resta = num1-num2
print('El resultado de la resta es',resta)

mul = num2 * num1
